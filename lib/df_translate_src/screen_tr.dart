import '_index.g.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

String screenTr(
  String input,
  String category, {
  Map<dynamic, dynamic> args = const {},
}) {
  return input.splitByLastOccurrenceOf('||').join('||$category.').tr(args: args);
}
